#  A dictionary is an unordered, mutable, and indexed collection of key-value pairs.
# 	•	Each item in a dictionary has two parts:
# Key → Value
# 	•	You access values using keys, not by index (like lists).
# 	•	Keys must be unique and immutable (e.g., strings, numbers, tuples).
# 	•	Values can be any data type (even other dictionaries).
# Example 
marks = {"Aniket":99, "raj":44, "Rudra":23}
print(marks)
# print(marks,type(marks))

print(marks["Aniket"])
print(marks["Rudra"])



