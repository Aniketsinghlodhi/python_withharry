# What is for loop in Python?
# A for loop in Python is used to iterate over a sequence (like a list, tuple, string, or range) 
# or other iterable objects. It allows you to execute a block of code multiple times, once for each item in the sequence. 
# The syntax of a for loop in Python is as follows:
# for item in sequence:
#     # code block to be executed   
# Here is an example of a for loop that iterates over a list of numbers and prints each number:
numbers = [1, 2, 3, 4, 5]
for number in numbers:
    print(number)   
# Output:
# 1
# 2
# 3
# 4     

