# What is while loop in Python?
# A while loop in Python is used to repeatedly execute a block of code as long as a specified condition is true. 
# The syntax of a while loop in Python is as follows:
# they are used when the number of iterations is not known beforehand. 
#sysntax:
# while condition:
#     # code block to be executed
# Here is an example of a while loop that prints numbers from 1 to 5:
count = 1
while count <= 5:
    print(count)
    count += 1
# Output:
# 1
# 2                         
# 3
# 4
# 5         
# Example 2: Print the first 10 multiples of 5 using a while loop
i = 1
while i <= 10:
    print(i * 5)
    i += 1  
# Example 3: Print a statement 110 times using a while loop
j = 1
while j <= 110:
    print("Aniket is the greatest person on earth")
    j += 1
# Output:
# Aniket is the greatest person on earth