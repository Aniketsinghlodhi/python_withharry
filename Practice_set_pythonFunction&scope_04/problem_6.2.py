def multiply (a,b):
    '''
    multiply two numbers 
    paramaters 
    a (int or float) : the first number:
    b (int or float): the second number:
returns:
int or float : the product of a and b
    
    
    '''
    return a*b

#Example usage 

print(multiply(5,3)) # 15 
print(multiply(2.5, 4)) # 10.0
