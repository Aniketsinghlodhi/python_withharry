#python suppot several built-in data types to store different kinds of values. The most commonly used data types in python are:
# 1. Integer (int): Represents whole numbers, both positive and negative, without decimal points.
# 2. Float (float): Represents real numbers with decimal points.
# 3. String (str): Represents sequences of characters, enclosed in single or double quotes.
# 4. Boolean (bool): Represents two values: True or False, often used for conditional statements and logical operations.    
name = "Aniket"     # string
print(name)
print(type(name))   
age = 21             # integer
print(age)
print(type(age))
height = 5.9        # float
print(height)
print(type(height)) 
is_student = True   # boolean
print(is_student)
print(type(is_student)) 
# Python also supports other data types like:
# 5. List: An ordered collection of items, which can be of different data types.
# 6. Tuple: An ordered, immutable collection of items.
# 7. Dictionary: A collection of key-value pairs.
# 8. Set: An unordered collection of unique items.
# Example of other data types:
# List
fruits = ["apple", "banana", "cherry"]
print(fruits)
print(type(fruits))
# Tuple
coordinates = (10.0, 20.0)
print(coordinates)
print(type(coordinates))
# Dictionary
student = {"name": "Aniket", "age": 21, "is_student":   True}
print(student)
print(type(student))
# Set
unique_numbers = {1, 2, 3, 4, 5}
print(unique_numbers)       
print(type(unique_numbers))
#  the type() function to check the data type of a variable in python.
number = 42
print(type(number))  # Output: <class 'int'>
pi = 3.14
print(type(pi))      # Output: <class 'float'>
message = "Hello, World!"
print(type(message)) # Output: <class 'str'>
is_valid = False
print(type(is_valid)) # Output: <class 'bool'>  



 