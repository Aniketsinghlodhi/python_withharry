# comments are used to explain code and make it more readable and ignored by the interpreter 
# single line comment starts with a hash symbol (#)
# This is a single line comment 
print("Hello, World!")  # This comment is after a code statement    
# multi-line comments can be created using triple quotes (''' or """)   
''' 
This is a multi-line comment        


It can span multiple lines
'''
print("Hello again!")
"""         
This is another multi-line comment
Using double quotes
"""
